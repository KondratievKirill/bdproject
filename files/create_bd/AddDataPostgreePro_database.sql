-- Примеры запросов для проверки
-- ============================================
-- Общий баланс пользователя 1
SELECT 
    u.username,
    SUM(a.balance) AS total_balance
FROM users u
JOIN accounts a ON u.id = a.user_id
WHERE u.id = 1
GROUP BY u.username;

-- Расходы по категориям за май 2026
SELECT 
    c.name AS category,
    SUM(t.amount) AS total_spent
FROM transactions t
JOIN categories c ON t.category_id = c.id
WHERE t.user_id = 1 
    AND c.type = 'expense'
    AND t.transaction_date >= '2026-05-01'
GROUP BY c.name
ORDER BY total_spent DESC;

-- Прогресс по целям
SELECT 
    name,
    target_amount,
    current_amount,
    ROUND((current_amount / target_amount) * 100, 2) AS progress_percent,
    deadline
FROM savings_goals
WHERE user_id = 1;